'''
산술 연산자: 산술 연산자는 기본적인 수학적 연산을 수행합니다.

+ : 덧셈
- : 뺄셈
* : 곱셈
/ : 나눗셈
% : 나머지 (모듈로 연산)


할당 연산자: 변수에 값을 할당하는 데 사용됩니다.
= : 할당


증가/감소 연산자: 변수 값을 증가시키거나 감소시킵니다.

++ : 증가 연산자
-- : 감소 연산자


비트 연산자: 비트 단위 연산을 수행합니다.
& : 비트 AND
| : 비트 OR
^ : 비트 XOR
~ : 비트 NOT
<< : 비트를 왼쪽으로 이동
>> : 비트를 오른쪽으로 이동


논리 연산자: 논리 연산을 수행합니다.
&& : 논리 AND
|| : 논리 OR
! : 논리 NOT


관계 연산자: 비교 연산을 수행하여 결과를 논리 값으로 반환합니다.
== : 같음
!= : 다름
> : 크다
< : 작다
>= : 크거나 같다
<= : 작거나 같다


조건부 연산자: 조건에 따라 값을 반환합니다.
? : : 삼항 조건 연산자
'''
